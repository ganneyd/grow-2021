///This represents what [category] the trophy belongs to
///[TROPHY_CATEGORY.RUN] trophy is rewarded based on how many runs the child has completed
///[TROPHY_CATEGORY.ENDURANCE] trophy is rewarded based on how long a child runs
///[TROPHY_CATEGORY.GOAL_COMPLETED] trophy is rewarded when a goal set by the child is completed
///[TROPHY_CATEGORY.XP] trophy is rewarded when a mile stone is reached in terms of XP points
///[TROPHY_CATEGORY.DISTANCE]

enum TROPHY_CATEGORY { RUN, ENDURANCE, DISTANCE, XP, GOAL_COMPLETED, UNKNOWN }
